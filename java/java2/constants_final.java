public class constants_final {
    public static void main(System[] args) {
        float p = 3.142857F;
        // or
        float pie = 3.142857f;
        final float pi = 3.1415926F;
        System.out.println(p);
        System.out.println(pie);
        System.out.println(pi);
        // this is constant
        // ie immutable
        // pi= 3.14; //this gives an error
    }

}
